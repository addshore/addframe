function printselect($name,$arr,$sel) {
	echo "<select name=\"$name\">\n";
	foreach($arr as $k=>$v) {
		echo "\t<option value=\"$k\"".
		     ($sel==$k?" selected":"").
		     ">$v</option>\n";
	}
	echo "</select>\n";
}

